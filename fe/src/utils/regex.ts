const Regex = {
  email: new RegExp(
    "/[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/",
    'g'
  ),
  phone: new RegExp(
    '/((^010)(?:\\d{4})(?:\\d{4})$)|(^010-(?:\\d{4})-(?:\\d{4})$)/',
    'gm'
  ),
  password: new RegExp('^(?=.*\\d)(?=.*[a-z])(?=.*[a-zA-Z]).{8,}$'),
};

export default Regex;
